export function getSummaryLengthLabel(
  locale: Locale,
  summaryLength: SummaryLength,
): string {
  const t = uiText[locale];

  if (summaryLength === "short") {
    return t.summaryShort;
  }

  if (summaryLength === "long") {
    return t.summaryLong;
  }

  return t.summaryMedium;
}
